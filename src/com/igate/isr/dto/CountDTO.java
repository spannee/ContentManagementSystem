package com.igate.isr.dto;



public class CountDTO {
	
	
	int techCount;
	int solutionCount;
	/*Method to get the technology count*/
	public int getTechCount() {
		
		return techCount;
	}
	/*Method to set the technology count*/
	public void setTechCount(int techCount) {
				
		this.techCount = techCount;
	}
	/*Method to get the solution count*/
	public int getSolutionCount() {
		
		return solutionCount;
	}
	/*Method to set the solution count*/
	public void setSolutionCount(int solutionCount) {
		
		
		this.solutionCount = solutionCount;
	}

}
